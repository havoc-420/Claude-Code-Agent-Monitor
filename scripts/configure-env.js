#!/usr/bin/env node
/**
 * configure-env.js — Interactive .env file configurator
 *
 * Usage:
 *   node scripts/configure-env.js          # full interactive
 *   node scripts/configure-env.js --reset   # reset to defaults and re-configure
 */

const fs = require("fs");
const path = require("path");
const readline = require("readline");

const ENV_PATH = path.join(__dirname, "..", ".env");
const ENV_EXAMPLE = path.join(__dirname, "..", ".env.example");

const FIELDS = [
  {
    key: "DASHBOARD_PORT",
    label: "Dashboard listen port",
    default: "4820",
    validate: (v) => (!isNaN(parseInt(v, 10)) && parseInt(v, 10) > 0) || "Must be a valid port number (1-65535)",
  },
  {
    key: "DASHBOARD_PUBLIC_URL",
    label: "Public URL (displayed in logs & client, e.g. http://1.2.3.4:4820)",
    default: "http://localhost:4820",
    validate: null,
  },
  {
    key: "DASHBOARD_ADMIN_PASSWORD",
    label: "Admin password (leave empty to disable auth)",
    default: "",
    sensitive: true,
    validate: null,
  },
  {
    key: "DASHBOARD_DB_PATH",
    label: "SQLite database path",
    default: "./data/dashboard.db",
    validate: null,
  },
  {
    key: "NODE_ENV",
    label: "Node environment",
    default: "production",
    options: ["production", "development"],
    validate: null,
  },
];

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function dim(text) {
  return `\x1b[2m${text}\x1b[0m`;
}

function bold(text) {
  return `\x1b[1m${text}\x1b[0m`;
}

function green(text) {
  return `\x1b[32m${text}\x1b[0m`;
}

function cyan(text) {
  return `\x1b[36m${text}\x1b[0m`;
}

function loadExisting() {
  const env = {};
  try {
    fs.readFileSync(ENV_PATH, "utf8")
      .split("\n")
      .filter((l) => l && !l.startsWith("#"))
      .forEach((line) => {
        const eq = line.indexOf("=");
        if (eq > 0) {
          env[line.slice(0, eq).trim()] = line.slice(eq + 1).trim().replace(/^["']|["']$/g, "");
        }
      });
  } catch {}
  return env;
}

function createRl() {
  return readline.createInterface({ input: process.stdin, output: process.stdout });
}

function question(rl, prompt) {
  return new Promise((resolve) => rl.question(prompt, resolve));
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

async function main() {
  const reset = process.argv.includes("--reset");

  console.log(bold("\n  Agent Dashboard — .env Configuration\n"));

  const existing = reset ? {} : loadExisting();

  const rl = createRl();
  const results = {};

  for (const field of FIELDS) {
    const current = existing[field.key];
    const hasCurrent = current !== undefined;

    // Show current value or indicate it's new
    if (hasCurrent && !reset) {
      const display = field.sensitive ? "*".repeat(current.length) : current;
      console.log(`  ${cyan(field.key)}=${dim(display)}`);
    } else {
      console.log(`  ${cyan(field.key)} ${dim("(not set)")}`);
    }

    let hint = "";
    if (field.options) hint = ` [${field.options.join("/")}]`;
    else if (field.sensitive && !hasCurrent) hint = " (hidden input)";
    else if (field.default) hint = ` (default: ${field.default})`;

    const prompt = `  > ${field.label}${hint}: `;

    while (true) {
      let answer;
      if (field.sensitive) {
        // readline doesn't hide input natively, use a workaround with stdout mute
        // For simplicity we just accept visible input but note it
        answer = await question(rl, prompt);
      } else {
        answer = await question(rl, prompt);
      }

      // Empty → keep existing or use default
      if (!answer && hasCurrent) {
        results[field.key] = current;
        break;
      }
      if (!answer && field.default) {
        results[field.key] = field.default;
        break;
      }
      if (!answer) {
        results[field.key] = "";
        break;
      }

      // Validate
      if (field.validate) {
        const err = field.validate(answer);
        if (err) {
          console.log(`    ${dim(`⚠ ${err}`)}`);
          continue;
        }
      }

      results[field.key] = answer;
      break;
    }

    console.log();
  }

  rl.close();

  // Generate .env content
  const lines = [
    "# Agent Dashboard Environment",
    "# Auto-generated by: node scripts/configure-env.js",
    "#",
    `# Generated at: ${new Date().toISOString()}`,
    "",
  ];

  for (const field of FIELDS) {
    const val = results[field.key] ?? "";
    const comment = `# ${field.label}`;
    lines.push(comment);
    lines.push(`${field.key}=${val}`);
    lines.push("");
  }

  const content = lines.join("\n").trimEnd() + "\n";

  fs.writeFileSync(ENV_PATH, content, "utf8");
  console.log(green(`  ✓ .env written to ${path.relative(process.cwd(), ENV_PATH)}`));
  console.log(dim("  Run `npm start` to apply.\n"));
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
